      SUBROUTINE INIT(X,N,V)
C=======================================================================
C     pg 429
C=======================================================================
C     INITIALIZE VECTOR V TO VALUE X
C=======================================================================
      IMPLICIT REAL*8(A-H,O-Z)
      DIMENSION V(*)
C-----------------------------------------------------------------------
      DO 10 I=1,N
10    V(I)=X
      RETURN
      END