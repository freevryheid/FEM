      SUBROUTINE MAJ(X1,X2,N,V1,V2)
C======================================================================
C     pg 429
C======================================================================
C     EXECUTE THE VECTOR OPERATION: V2=X1*V1 + X2*V2
C       X1,X2:SCALARS   V1,V2:VECTORS
C======================================================================
      IMPLICIT REAL*8(A-H,O-Z)
      DIMENSION V1(*),V2(*)
C----------------------------------------------------------------------
      DO 10 I=1,N
10    V2(I)=X1*V1(I)+X2*V2(I)
      RETURN
      END